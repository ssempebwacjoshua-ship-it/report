import { z } from "zod";
import { assetUrlSchema } from "../utils/assetUrls";

function blankStringToUndefined(value: unknown) {
  if (typeof value !== "string") return value;
  return value.trim() === "" ? undefined : value;
}

const optionalTrimmedString = (defaultValue = "") =>
  z.preprocess(blankStringToUndefined, z.string().trim().optional().default(defaultValue));

const optionalEmailString = (message: string) =>
  z.preprocess(
    blankStringToUndefined,
    z.string().trim().email(message).optional().default(""),
  );

const optionalUrlString = (message: string) =>
  z.preprocess(
    blankStringToUndefined,
    z.string().trim().url(message).optional().default(""),
  );

const optionalPhoneString = z.preprocess(
  blankStringToUndefined,
  z
    .string()
    .trim()
    .regex(/^[+]?[0-9\s()-]+$/, "Enter a valid phone number or leave blank.")
    .optional()
    .default(""),
);

export const SETTING_SECTIONS = [
  "school",
  "academic",
  "reports",
  "reportPersonalization",
  "marksheets",
  "ocr",
  "grading",
  "approval",
  "appearance",
] as const;

export type SettingSection = (typeof SETTING_SECTIONS)[number];

export const assessmentTypeSchema = z.enum(["BOT", "MOT", "EOT", "TERM_SUMMARY"]);

export const schoolProfileSchema = z.object({
  schoolName: z.string().trim().min(1, "School name is required."),
  schoolCode: z.string().trim().min(1, "School code is required."),
  address: optionalTrimmedString(),
  phone: optionalPhoneString,
  email: optionalEmailString("Enter a valid email address or leave blank."),
  website: optionalUrlString("Enter a valid URL or leave blank."),
  headTeacherName: z.string().trim().default("Head Teacher"),
  reportFooterText: z.string().trim().default("This report was generated by School Connect Reports First."),
  marksheetFooterText: z.string().trim().default("Blank means missing, not zero."),
  logoUrl: assetUrlSchema,
  schoolSections: z.array(z.enum(["NURSERY", "PRIMARY", "SECONDARY", "COMBINED"])).min(1).default(["SECONDARY"]),
});

export const academicSetupSchema = z.object({
  activeAcademicYear: z.string().trim().min(1, "Active academic year is required."),
  activeTerm: z.string().trim().min(1, "Active term is required."),
  defaultAssessmentType: assessmentTypeSchema.default("EOT"),
  supportedAssessmentTypes: z.array(assessmentTypeSchema).min(1, "Select at least one assessment type."),
  termStartDate: z.string().trim().regex(/^\d{4}-\d{2}-\d{2}$/, "Use YYYY-MM-DD."),
  termEndDate: z.string().trim().regex(/^\d{4}-\d{2}-\d{2}$/, "Use YYYY-MM-DD."),
}).superRefine((value, ctx) => {
  if (value.termStartDate > value.termEndDate) {
    ctx.addIssue({ code: "custom", path: ["termEndDate"], message: "Term end date must be after the start date." });
  }
  if (!value.supportedAssessmentTypes.includes(value.defaultAssessmentType)) {
    ctx.addIssue({ code: "custom", path: ["defaultAssessmentType"], message: "Default assessment must be supported." });
  }
});

