export const reportPersonalizationGradeBandSchema = z.object({
  sortOrder: z.number().int().min(0).default(0),
  name: z.string().trim().min(1, "Band name is required."),
  minScore: z.number().int().min(0).max(100),
  maxScore: z.number().int().min(0).max(100),
  grade: z.string().trim().min(1, "Grade label is required."),
  points: z.number().optional(),
  remark: z.string().trim().default(""),
}).superRefine((band, ctx) => {
  if (band.minScore > band.maxScore) {
    ctx.addIssue({ code: "custom", path: ["minScore"], message: "Minimum score cannot be greater than maximum score." });
  }
});

export const reportPersonalizationCommentSchema = z.object({
  sortOrder: z.number().int().min(0).default(0),
  type: z.enum(["classTeacher", "headteacher", "subject", "conduct", "attendance"]),
  performanceBand: z.enum(["excellent", "good", "average", "weak", "poor"]).optional(),
  comment: z.string().trim().min(1, "Comment text is required."),
  active: z.boolean().default(true),
});

export const reportPersonalizationSchema = z.object({
  branding: reportPersonalizationBrandingSchema,
  layout: reportPersonalizationLayoutSchema,
  gradingScheme: z.array(reportPersonalizationGradeBandSchema).min(1, "Add at least one grade band."),
  commentBank: z.array(reportPersonalizationCommentSchema).default([]),
}).superRefine((value, ctx) => {
  const sorted = value.gradingScheme.slice().sort((a, b) => a.sortOrder - b.sortOrder);
  const coverage = new Array(101).fill(0) as number[];
  sorted.forEach((band, index) => {
    for (let score = band.minScore; score <= band.maxScore; score += 1) {
      coverage[score] += 1;
      if (coverage[score] > 1) {
        ctx.addIssue({ code: "custom", path: ["gradingScheme", index], message: `Grade band overlaps at score ${score}.` });
        break;
      }
    }
  });
  for (let score = 0; score <= 100; score += 1) {
    if (coverage[score] === 0) {
      ctx.addIssue({ code: "custom", path: ["gradingScheme"], message: `Grading scheme has a gap at score ${score}.` });
      break;
    }
  }
});

