export const gradeSchema = z.object({
  label: z.enum(["D1", "D2", "C3", "C4", "C5", "C6", "P7", "P8", "F9"]),
  minScore: z.number().int().min(0).max(100),
  maxScore: z.number().int().min(0).max(100),
  descriptor: z.string().trim().optional().default(""),
}).superRefine((grade, ctx) => {
  if (grade.minScore > grade.maxScore) {
    ctx.addIssue({ code: "custom", path: ["minScore"], message: "Minimum score cannot be greater than maximum score." });
  }
});

export const gradingScaleSchema = z.object({
  grades: z.array(gradeSchema).length(9, "All nine grades must be configured."),
}).superRefine((value, ctx) => {
  const labels = value.grades.map((grade) => grade.label);
  for (const expected of ["D1", "D2", "C3", "C4", "C5", "C6", "P7", "P8", "F9"]) {
    if (!labels.includes(expected as (typeof labels)[number])) {
      ctx.addIssue({ code: "custom", path: ["grades"], message: `Missing grade ${expected}.` });
    }
  }

  const coverage = new Array(101).fill(0) as number[];
  value.grades.forEach((grade, index) => {
    for (let score = grade.minScore; score <= grade.maxScore; score += 1) {
      coverage[score] += 1;
      if (coverage[score] > 1) {
        ctx.addIssue({ code: "custom", path: ["grades", index], message: `Grade ${grade.label} overlaps at score ${score}.` });
        break;
      }
    }
  });
  const missing = coverage.findIndex((count) => count === 0);
  if (missing >= 0) {
    ctx.addIssue({ code: "custom", path: ["grades"], message: `Grading scale has a gap at score ${missing}.` });
  }
});

